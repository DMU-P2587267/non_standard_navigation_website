function toggleFlip(card) {
    card.classList.toggle('flipped');
  }
/* The function says that the element(s) with the class 'card' will be flipped when it gets toggled.
   By the term toggled it means that it removes an element and adds another in its place and can them return it to the first element.
   In this case the element with class gets changed with the element that has toggled as a class on it. */  